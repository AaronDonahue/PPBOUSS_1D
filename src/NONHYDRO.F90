      SUBROUTINE NONHYDRO
      
      USE READ_DGINP, ONLY : INONHYDRO
      
      IMPLICIT NONE
      
      SELECT CASE (INONHYDRO)
        CASE(2)
          CALL NONHYDRO_MU2
        CASE(4)
          CALL NONHYDRO_MU4
        CASE DEFAULT
          CALL NONHYDRO_MU0                
      END SELECT
!       
      RETURN
      END SUBROUTINE NONHYDRO
!..........................................................................!
!==========================================================================!
!..........................................................................!
      SUBROUTINE NONHYDRO_MU0
      
      USE GLOBALS, ONLY : IRK,PD,PB
      
      IMPLICIT NONE
      
      PD(:,:,IRK) = 0.D0
      PB(:,:,IRK) = 0.D0
      
      RETURN
      END SUBROUTINE NONHYDRO_MU0
!..........................................................................!
!==========================================================================!
!..........................................................................!
      SUBROUTINE NONHYDRO_MU2
      
      USE GLOBALS, ONLY : IRK,PD,PB
      
      IMPLICIT NONE
      
      PD(:,:,IRK) = 0.D0
      PB(:,:,IRK) = 0.D0
      
      STOP
      
      RETURN
      END SUBROUTINE NONHYDRO_MU2 
!..........................................................................!
!==========================================================================!
!..........................................................................!
      SUBROUTINE NONHYDRO_MU4
      
      USE GLOBALS, ONLY : IRK,PD,PB
      
      IMPLICIT NONE
      
      PD(:,:,IRK) = 0.D0
      PB(:,:,IRK) = 0.D0
      
      STOP
      
      RETURN
      END SUBROUTINE NONHYDRO_MU4
